package productPaymentRepository;

public class UseIngredientAmount {
	
	// variable
	private int ingredientNumber = 0;
	private int productNumber = 0;
	private int totalAmount = 0;
	
	// constructor
	public UseIngredientAmount() {
		
	}
	
	// getter and setter
	public int getIngredientNumber() {
		return ingredientNumber;
	}

	public void setIngredientNumber(int ingredientNumber) {
		this.ingredientNumber = ingredientNumber;
	}

	public int getProductNumber() {
		return productNumber;
	}

	public void setProductNumber(int productNumber) {
		this.productNumber = productNumber;
	}

	public int getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(int totalAmount) {
		this.totalAmount = totalAmount;
	}
		
}
